package variable;

public class Var2 {
    public static void main(String[] args) {
        // 누군가는 10, 100 ,1000을 입력할수도 있다.
        // 그때 그때 마다 바꿔주기가 번거롭다는 것
        // 그래서 변수 선언 및 초기화를 하는 것이다.

        // 변수 선언 및 초기화
        int a; // 변수 선언
        a = 20; // 10 -> 20으로 변경

        System.out.println(a); // 10 -> 20
        System.out.println(a);
        System.out.println(a);
    }
}
